void B1PrimaryGeneratorAction::GeneratePrimaries(G4Event* anEvent)
{
  //this function is called at the begining of ecah event
  //

  // In order to avoid dependence of PrimaryGeneratorAction
  // on DetectorConstruction class we get Envelope volume
  // from G4LogicalVolumeStore.
  
  G4double envSizeXY = 0;
  G4double envSizeZ = 0;

  if (!fEnvelopeBox)
  {
    G4LogicalVolume* envLV
      = G4LogicalVolumeStore::GetInstance()->GetVolume("Envelope");
    if ( envLV ) fEnvelopeBox = dynamic_cast<G4Box*>(envLV->GetSolid());
  }

  if ( fEnvelopeBox ) {
    envSizeXY = fEnvelopeBox->GetXHalfLength()*2.;
    envSizeZ = fEnvelopeBox->GetZHalfLength()*2.;
  }  
  else  {
    G4ExceptionDescription msg;
    msg << "Envelope volume of box shape not found.\n"; 
    msg << "Perhaps you have changed geometry.\n";
    msg << "The gun will be place at the center.";
    G4Exception("B1PrimaryGeneratorAction::GeneratePrimaries()",
     "MyCode0002",JustWarning,msg);
  }

  G4double size = 1.0*1./3000.; 
  G4double x0 = size * envSizeXY * (G4UniformRand()-0.5);
  G4double y0 = size * envSizeXY * (G4UniformRand()-0.5);
  G4double z0 = -0.5 * envSizeZ;

	
	G4double zf = -0.5*30.+10.;
  G4double xf = 3.6397023426620234*(G4UniformRand()-0.5);
  G4double yf;
	G4double l;	

  G4int flag=1;

	while(flag)
	{

		yf = 3.6397023426620234*(G4UniformRand()-0.5);

		l = xf*xf+yf*yf;

		if(l<=13.247433143179421)
		{
				flag=0;
		}

	}

	G4double xi = size*30.*(G4UniformRand()-0.5);
	G4double yi = size*30.*(G4UniformRand()-0.5);
	G4double zi = -0.5*30.;
//fParticleGun->SetParticleMomentumDirection(G4ThreeVector(0.,0.,1.));

  G4double px = xf-xi;
	G4double py = yf-yi;
	G4double pz = zf-zi;


	fParticleGun->SetParticleMomentumDirection(G4ThreeVector(px,py,pz));
  
  fParticleGun->SetParticlePosition(G4ThreeVector(x0,y0,z0));

  fParticleGun->GeneratePrimaryVertex(anEvent);
}

